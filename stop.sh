#!/usr/bin/env bash
# ==========================================================
#  CRM 知识库问答系统 - 停止脚本
# ==========================================================
set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
cd "$SCRIPT_DIR"

echo ""
echo "========================================================"
echo "  停止 CRM 知识库问答系统"
echo "========================================================"

# ---------- 1. 停止 Web 服务 ----------
echo "[步骤 1] 停止 Web 服务..."
pkill -f "uvicorn app.main:app" 2>/dev/null && echo "  Web 服务已停止 ✓" || echo "  Web 服务未运行"

# ---------- 2. 停止 Milvus ----------
echo ""
echo "[步骤 2] 停止 Milvus..."
docker compose down
echo "  Milvus 已停止 ✓"

echo ""
echo "========================================================"
echo "  所有服务已停止"
echo "========================================================"
echo ""
